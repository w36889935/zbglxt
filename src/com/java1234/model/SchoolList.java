package com.java1234.model;

public class SchoolList {
	private  String schoolCode; //学校编号
	private  String	 schoolName;//学校名字
	private  String	 inXian;//县
	private  String  inShi;//市
	private  String	 inSheng;//省
	public String getSchoolCode() {
		return schoolCode;
	}
	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getInXian() {
		return inXian;
	}
	public void setInXian(String inXian) {
		this.inXian = inXian;
	}
	public String getInShi() {
		return inShi;
	}
	public void setInShi(String inShi) {
		this.inShi = inShi;
	}
	public String getInSheng() {
		return inSheng;
	}
	public void setInSheng(String inSheng) {
		this.inSheng = inSheng;
	}
	
	
		   
}
